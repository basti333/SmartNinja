mood = input("How are you feeling today? \n")


if (mood == "happy"):
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Why?")

elif mood == "excited":
    print("Awesome!")

elif mood == "relaxed":
    print("Let's chill!")

else:
    print("Cheer up, mate!")


